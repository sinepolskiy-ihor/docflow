<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Біля кожного документа у
вигляді значків є меню основних дій (у порядку розташування): завантаження останньої
версії, перехід на сторінку друку картки документа, перехід на сторінку
ініціювання розсилки цього документа, список респондентів (якщо йде чи
завершилась розсилка документа), видалення документа разом з усіма його
версіями. Видаляти документ може лише той користувач, який його додав.<o:p></o:p></span></p>

