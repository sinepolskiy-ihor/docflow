<h1 align=center style='text-align:center'><span lang=UK style='font-family:
"Times New Roman";mso-ansi-language:UK'>Початок роботи<o:p></o:p></span></h1>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Система електронного
документообігу Запорізького національного університету створена для реєстрації
документів згідно з офіційною номенклатурою, розсилки на відповідні підрозділи
та контролю виконання. Система знаходиться у локальній мережі університету і
побудована у вигляді </span><span lang=EN-US style='font-size:14.0pt;
mso-ansi-language:EN-US'>web</span><span style='font-size:14.0pt'>-</span><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>ресурсу; підтримується
лабораторією інформаційних систем і комп’ютерних технологій ЗНУ.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Принцип роботи: кожен
зареєстрований підрозділ має відповідальних осіб, що можуть вносити документи
до системи, робити правки і редагувати їхні версії, розсилати ці документи
(ініціювати документообіг) на обрані підрозділи і перевіряти хід ознайомлення
чи виконання положень у цих документах через зворотний зв'язок (надання
підтвердження / відповіді).<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Ресурс знаходиться за
