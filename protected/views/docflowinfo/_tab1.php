<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Усі розсилки знаходяться
у списку (віджеті) з можливістю сортування і пошуку або фільтрації по деяким
стовпчикам. При текстовій фільтрації відшукуються ті записи, де частина тексту
така ж, як у полі біля назви стовпчика. Дата відображається у системному
форматі «рік-місяць-число».<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Зліва від кожної вхідної
розсилки знаходиться меню основних можливостей. Дія «Завантажити» завантажує останню
версію надісланого документа або </span><span lang=EN-US style='font-size:14.0pt;
mso-ansi-language:EN-US'>zip</span><span class=SpellE><span lang=UK
style='font-size:14.0pt;mso-ansi-language:UK'>-архів</span></span><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>, якщо розсилається
декілька документів. Дія «Переслати» дозволяє ініціювати процес документообігу
з такими параметрами, як в обраному. Дія «Надати підтвердження» відкриває
модальне вікно для зворотного зв’язку із тим підрозділом, що розпочав розсилку.<o:p></o:p></span></p>

