<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>У формі відповіді можна
написати текст відповіді (коментар) і прикріпити документ із тих, що надійшли
або створені вашим підрозділом. Але ці дії не є обов’язковими. Після натиснення
на кнопку «Зберегти» розсилка буде знаходитись у вкладці «Архів».<o:p></o:p></span></p>

