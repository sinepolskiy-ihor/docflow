<h2 align=center style='text-align:center'><span lang=UK style='font-family:
"Times New Roman";mso-ansi-language:UK;font-style:normal;mso-bidi-font-style:
italic'>Вхід у систему<o:p></o:p></span></h2>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Після входу в систему
користувачеві відображаються усі вхідні та власні розсилки (актуальні процеси
документообігу).<o:p></o:p></span></p>

