lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>УВАГА! <b
style='mso-bidi-font-weight:normal'>При отриманні нових розсилок необхідно
завантажити усі документи в них і надати підтвердження одразу після
ознайомлення з ними (для цього у більшості випадків достатньо натиснути на
елемент меню «Надати підтвердження» і кнопку «Зберегти» у модальному вікні).</b><o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;text-indent:35.45pt'><span
lang=UK style='font-size:14.0pt;mso-ansi-language:UK'>Також є можливість
